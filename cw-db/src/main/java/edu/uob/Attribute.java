package edu.uob;

public class Attribute {
    String name;
    String dataType;

    void setDataType(String dataType) {
        this.dataType = dataType;
    }

    String getDataType() {
        return dataType;
    }

    void setName(String name) {
        this.name = name;
    }

    void getName() {
        this.name = name;
    }
}
